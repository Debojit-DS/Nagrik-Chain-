@router.get("/officer/{chin}", response_model=List[AuditLogResponse])
def get_officer_audit_trail(
    chin: str, 
    db: Session = Depends(get_db),
    current_officer: TokenClaims = Depends(require_officer_integrity)
):
    """
    Retrieves the complete action log of a government officer. Gated by officer integrity.
    """
    if current_officer.sub != chin and current_officer.role != "court":
        raise HTTPException(status_code=403, detail="Unauthorized to view this officer's logs")
        
    trail = AuditService.get_officer_trail(db, chin)
