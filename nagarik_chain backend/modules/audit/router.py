@router.get("/tx/{blockchain_tx}", response_model=AuditLogResponse)
def lookup_audit_by_blockchain_tx(
    blockchain_tx: str, 
    db: Session = Depends(get_db),
    current_user: TokenClaims = Depends(require_chin_auth)
):
    """
    Fetches the audit database entry linked to a specific blockchain transaction ID.
    """
    log = AuditService.get_by_tx(db, blockchain_tx)
    if not log:
        raise HTTPException(status_code=404, detail="Audit log entry not found for this blockchain transaction")
    return log
